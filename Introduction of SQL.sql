# SQL Commands

-- There are five SQL Commands. 

# 1. DDL :- Data Definition Language
-- 			It is used to create the structure of database or table.

# 2. DML :- Data Manipulation Language
-- 			It is used to manipulate the table.

# 3. DCL :- Data Control Language

# 4. TCL :- Transaction Control Language

# 5. DQL :- Data Query Language

#-----------------------------------------------------------------------------------------------------------------------------------

# DDL :- Data Definition Language

# 1. CREATE :- 	It is used to create a table or a database.
# 2. DROP :- 	It is used to delete the database or table.
# 3. ALTER :- 	It is used to add a column in a table. 
-- 				It is used to delete a column from the table.
-- 				Its also rename the headers of column. 
-- 				It is used to change the datatype of a column. 
# 4. TRUNCATE :- Truncate are used to delete the data from a table and keep the format. 

#------------------------------------------------------------------------------------------------------------------------------------

# DML :- Data Manipulation Language

# 1. INSERT :- With the help of this command we can add values inn particular rows. 
# 2. UPDATE :- It is used to add the value or replace the value in a particular cell.
# 3. DELETE :- Delete are used to delete the rows from table according to user. 

#------------------------------------------------------------------------------------------------------------------------------------

# DCL :- Data Control Langugae

# 1. GRANT :- It is used to give the permission to other user to manipulate the data. 
# 2. REVOKE :- Its restrict the permission. 

#-------------------------------------------------------------------------------------------------------------------------------------

# TCL :- Transaction Control Language

# 1. SAVEPOINT :- Its save the data temporary base in database. 
# 2. ROLLBACK :- Its take a step back in data. 
# 3. COMMIT :- Its save the data permamently in database.

#-------------------------------------------------------------------------------------------------------------------------------------

# DQL :- Data Query Language

# 1. SELECT :- Its retrive the data from the database or retrive the database from the cloud. 

